const DAYS = ["Mon", "Tue", "Wed", "Thu", "Fri", "Sat", "Sun"];

export const SHIFTS: Shift[] = STAFF.flatMap((s, idx) =>
  DAYS.map((day, di) => {
    const dayOff = (idx + di) % 7 === 6;
    if (dayOff || s.status === "on-leave") return null;
    const open = di % 2 === 0;
    return {
      id: `sh_${s.id}_${day}`,
      staffId: s.id,
      staffName: s.name,
      date: day,
      start: open ? "06:30" : "12:30",
      end: open ? "14:30" : "20:30",
      role: s.role,
      status: di < 4 ? "completed" : di === 4 ? "confirmed" : "scheduled",
    } satisfies Shift;
  }).filter(Boolean) as Shift[],
);

